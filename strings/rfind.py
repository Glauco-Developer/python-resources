"""
This script demonstrates the use of the `rfind()` method in Python to find the last occurrence of a specific substring within a string.

The `rfind()` method:
- Returns the position of the last occurrence of the specified substring.
- Returns `-1` if the substring is not found.
"""
text = "Python is a high-level, general-purpose programming language"
result = text.rfind("e")
print(result)
