"""
This script demonstrates how to use string slicing in Python to extract a portion of a string.

Key Concepts:
1. **String Indexing**:
   - Strings in Python are sequences, and each character in the string has an index.
   - Indexing starts at 0 for the first character.

2. **String Slicing**:
   - You can extract a portion of a string using the syntax `string[start:end]`.
   - The `start` index is inclusive, and the `end` index is exclusive.

"""
text = "Python is amazing"
word_python = text[0:6]
print(word_python)
